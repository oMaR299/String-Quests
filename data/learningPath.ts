// Flatten all nodes for lookup
export const ALL_PATH_NODES: PathNode[] = LEARNING_PATH.flatMap(s => s.nodes);

// Get node by ID
export function getPathNode(id: string): PathNode | undefined {
  return ALL_PATH_NODES.find(n => n.id === id);
}

// Star calculation from score percentage
export function getStars(scorePercent: number): 0 | 1 | 2 | 3 {
  if (scorePercent >= 95) return 3;
  if (scorePercent >= 75) return 2;
  if (scorePercent >= 50) return 1;
  return 0;
}
